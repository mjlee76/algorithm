package 백준.배열;

import java.util.*;

public class 평균은넘겠지 {
    
    public static void main(String arg[]){

        Scanner sc = new Scanner(System.in);
        int c = sc.nextInt();
        int s;
        int []grade = new int[s];
        for(int i=0; i<c; i++){
            s = sc.nextInt();
            for(int j=0; j<s; j++){
                grade[j] = sc.nextInt();
            }
        }
    }
}
